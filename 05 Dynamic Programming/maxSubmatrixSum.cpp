/*
Description:
    Given a rectangular matrix and dimesion (representing the dimensions of a square matrix), return a number representing the
    maximum sum of all potential subarrays

Time Complexity:
    O((n-d)(m-d)d^2) for an m*n matrix where d = (min(m,n) - dimension)
    
Space Complexity:
    O(1), only use local variables to store constant values
*/

#include <iostream>
#include <deque>

using namespace std;

//Time Complexity: O(n^2)
int maxSubmatrix(int size, deque<deque<int>> matrix) {
    int solution = 0;
    int potentialSolution = INT_FAST8_MIN;

    for(int row = 0; row <= matrix.size() - size; row++) {
        for(int col = 0; col <= matrix[row].size() - size; col++) {
            potentialSolution = 0;

            for(int subRow = row; subRow < row + size; subRow++) {
                for(int subCol = col; subCol < col + size; subCol++) {
                    potentialSolution += matrix[subRow][subCol];
                }
            }

            if(potentialSolution >= solution) solution = potentialSolution;


        }
    }

    return solution;
}

